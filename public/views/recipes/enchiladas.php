        <h3>Ingredients</h3>
    <ul>
        <li>Taco seasoning</li>
        <li>1 lb ground chicken</li>
        <li>Thin flour tortillas</li>
        <li>1 or 2 - 10 oz can of enchilada sauce (red or green)</li>
        <li>1/2 medium onion</li>
        <li>1 - 8oz block Monterey Jack Cheese</li>
        <li>1/2 - 8oz block sharp cheddar cheese</li>
        <li>1 tbsp sour cream</li>
        <li>1 jalapeno or 1 tbsp canned green chiles (optional)</li>
        <li>Olive oil</li>
    </ul>

    <hr>
    <ol>
        <li>Pre-heat oven to 400 degrees</li>
        <li>Shred the cheeses</li>
        <li>Dice onion and jalapeno. I deseed the jalapeno, but that is based on preference. Add to large pan on medium high heat</li>
        <li>Add chicken once vegg is softened, follow steps for taco meat</li>
        <li>Once meat is cooked and spice seasoning is added and incorporated, let cool slightly, then add sour cream</li>
        <li>This optional step is to add some taco sauce if you have any</li>
        <li>Lightly oil a casserole dish to avoid sticking</li>
        <li>Place about 1 - 1/2 ozs of meat mixture in a tortilla. Add a very small amount of cheese and then roll the tortilla up. Place in dish seam side down</li>
        <li>Repeat this until pan is full. Pour enchilada over the top of the enchiladas, making sure to evenly coat and get in between each enchilada. Can use more than one can if needed, depending on how saucy you want it</li>
        <li>Spread all cheese on top of enchiladas evenly and place in oven until melted and slightly browned. About 10 - 15 mins</li>
        <li>Let rest 5 minutes and enjoy!</li>

    </ol>

