        <h3>Ingredients</h3>
    <ul>
        <li> 1 can 28 oz San Marzano whole peeled tomatoes</li>
        <li>4 - 8 cloves garlic</li>
        <li>1/2 tsp oregano leaves</li>
        <li>1/4 tsp crushed red pepper flakes</li>
        <li>1/2 tsp pepper</li>
        <li>salt to taste</li>
        <li>Olive oil</li>
    </ul>

    <hr>
    <ol>
        <li>Finely dice or use press for garlic.</li>
        <li>Crush tomatoes with hands, remove and discard any skins or tough cores. Swish some water in can to get remaining tomato juice. About 1/4 cup water</li>
        <li>Coat bottom of large sauce pan with olive oil over medium heat. </li>
        <li>Add garlic and lightly sautee until softened. Do not brown. </li>
        <li>Add in tomato's, tomato water, and spices.</li>
        <li>Salt to taste, usually 1/2 - 1 tsp kosher salt.</li>
        <li>Continue stirring until sauce reaches desired thickness. </li>

    </ol>

