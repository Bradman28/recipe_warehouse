        <h3>Ingredients</h3>
    <ul>
        <li>Chuck roast 4 lbs (3 - 5 would be fine)</li>
        <li>1 cup red wine</li>
        <li>1 can beef broth 14 oz</li>
        <li>1 medium onion</li>
        <li>3 - 4 cloves garlic</li>
        <li>1/2 tsp thyme</li>
        <li>1 bay leaf</li>
        <li>Flour</li>
        <li>Potatoes</li>
        <li>Olive Olive</li>
    </ul>

    <hr>
    <ol>
        <li>Preheat oven to 300 degrees fehrenheit</li>
        <li>Roughly chop onion </li>
        <li>Generously salt/pepper meat, then coat with flour</li>
        <li>Heat olive oil in pan on medium high heat, sear all sides of roast</li>
        <li>Line sides of pan with onion and garlic</li>
        <li>Remove from heat, add wine around edges of pan, return to heat</li>
        <li>Let simmer until reduced by half</li>
        <li>Transfer to baking pot and add remaining stock</li>

        <li>Put in oven for 2 hrs covered. In the meantime, roughly chop potatoes, carrots, celery. Add in after the 2 hours. Lightly season.</li>
        <li>Cover and cook another 2 hours until fork tender. Remove cover to reduce if needed</li>

    </ol>

