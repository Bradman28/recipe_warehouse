    <div class="shape">
        <h3>Ingredients</h3>
    <ul>
        <li>1 cup all purpose flour</li>
        <li>1.5 tsp baking powder</li>
        <li>1/4 tsp kosher salt</li>
        <li>2/3 cup heavy cream</li>
        <li>Melted butter</li>
    </ul>

    <hr>
    <ol>
        <li>Preheat oven to 425 degrees</li>
        <li>Mix all ingredients in a bowl</li>
        <li>Move to flat floured surface, knead a few times</li>
        <li>Shape into biscuit, or use ice cream scoop, brush top with melted butter</li>
        <li>Place on baking sheet</li>
        <li>Remove from oven when tops get golden brown (about 15 mins), brush with more butter</li>
        <li>Serve with jelly or gravy sauce</li>

    </ol>

